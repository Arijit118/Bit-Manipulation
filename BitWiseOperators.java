package BitManipulation;

public class BitWiseOperators {
    public static void main(String args[]) {
        int a = 5;
        int b = 6;

        //Binary AND
        System.out.println(a & b);

        //Binary OR
        System.out.println(a | b);

        //Binary XOR
        System.out.println(a ^ b);

        //Binary 1's complement
        System.out.println(~0);

        //Binary Left Shift
        System.out.println(5 << 2);

        //Binary Right Shift
        System.out.println(6 >> 1);
    }
}
